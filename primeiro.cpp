#include <iostream>
using namespace std;
int main() {
    int x,y;
    cout <<"Digite um valor inteiro: ";
    cin >> x;
    cout <<"Digite um segundo valor inteiro: ";
    cin >> y;
    cout <<"x+y =" << x+y << "\n";
    cout <<"x-y =" << x-y << "\n";  
    cout <<"x*y =" << x*y << "\n";
    cout <<"x/y =" << x/y << "\n";
    cout <<"modulo" << x%y << "\n";
    return 0;

}
